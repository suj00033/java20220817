package java20220817.ex.ch06.Pro.p17;

public class Printer {
	static void printerln(int x) {
		System.out.println(x);
	}
	
	static void printerln(boolean x) {
		System.out.println(x);
	}
	
	static void printerln(double x) {
		System.out.println(x);
	}
	
	static void printerln(String x) {
		System.out.println(x);
	}
}
